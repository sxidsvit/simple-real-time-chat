// This script creates a Node.js application that:

// - Establishes a web server using Express.js.
// - Initializes a Socket.IO server to enable real-time communication.
// - Handles client connections and disconnections.
// - Implements a simple chat functionality by broadcasting messages received from clients to all connected clients.

const express = require("express");
const http = require("http");
const { Server } = require("socket.io");

const app = express();
const server = http.createServer(app);
const io = new Server(server, {
  cors: {
    origin: "http://localhost:5173",
    methods: ["GET", "POST"],
  },
});

io.on("connection", (socket) => {
  console.log("a user connected");

  socket.on("chat message", (msg) => {
    io.emit("chat message", msg);
  });

  socket.on("disconnect", () => {
    console.log("user disconnected");
  });
});

server.listen(3000, () => {
  console.log("listening on *:3000");
});